package com.shopfront.entity;

public class Attrbute {

	private int ID;//主键ID
	
	private int GoodsID;//商品ID
	
	private int AttributeID;//属性ID
	
	private String AttributeName;//属性值
	
	private String AttributeValue;//属性值
	
	private int InventoryNum;//库存量
	
	private double Money;//商品价钱
	
	private int AttrSum;

	public Attrbute() {
	}

	public int getID() {
		return ID;
	}

	public void setID(int iD) {
		ID = iD;
	}

	public int getGoodsID() {
		return GoodsID;
	}

	public void setGoodsID(int goodsID) {
		GoodsID = goodsID;
	}

	public int getAttributeID() {
		return AttributeID;
	}

	public void setAttributeID(int attributeID) {
		AttributeID = attributeID;
	}

	
	
	public String getAttributeName() {
		return AttributeName;
	}

	public void setAttributeName(String attributeName) {
		AttributeName = attributeName;
	}

	public String getAttributeValue() {
		return AttributeValue;
	}

	public void setAttributeValue(String attributeValue) {
		AttributeValue = attributeValue;
	}

	public int getInventoryNum() {
		return InventoryNum;
	}

	public void setInventoryNum(int inventoryNum) {
		InventoryNum = inventoryNum;
	}

	public double getMoney() {
		return Money;
	}

	public void setMoney(double money) {
		Money = money;
	}

	public int getAttrSum() {
		return AttrSum;
	}

	public void setAttrSum(int attrSum) {
		AttrSum = attrSum;
	}
	
	
	
}
