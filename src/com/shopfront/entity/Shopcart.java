package com.shopfront.entity;
/**
 * 购物车信息表 实体类
 * @author A
 *
 */
public class Shopcart {

	private int ID; // ID
	private int shopcartID; // 信息ID
	private String GoodsinfoName; // 商品名称
	private int Num; // 购买数量
	private double GoodsinfoPrice; // 商品价格
	private double Goodsinfosum; // 商品总价
	private int GoodsinfoID; // 商品ID
	private int MemberID;//用户ID
	private double GoodsWeight; // 商品重量
	private String GoodsinfoAttrName;//商品属性名
	private String GoodsinfoAttrValue;//商品属性值
	
	//无参构造
	public Shopcart(){
		
	}

	
	
	public int getID() {
		return ID;
	}



	public void setID(int iD) {
		ID = iD;
	}


	

	public double getGoodsinfoPrice() {
		return GoodsinfoPrice;
	}



	public void setGoodsinfoPrice(double goodsinfoPrice) {
		GoodsinfoPrice = goodsinfoPrice;
	}



	public int getMemberID() {
		return MemberID;
	}



	public void setMemberID(int memberID) {
		MemberID = memberID;
	}



	public int getShopcartID() {
		return shopcartID;
	}

	public void setShopcartID(int shopcartID) {
		this.shopcartID = shopcartID;
	}

	public String getGoodsinfoName() {
		return GoodsinfoName;
	}

	public void setGoodsinfoName(String goodsinfoName) {
		GoodsinfoName = goodsinfoName;
	}

	public int getNum() {
		return Num;
	}

	public void setNum(int num) {
		Num = num;
	}

	public double getGoodinfoPrice() {
		return GoodsinfoPrice;
	}

	public void setGoodinfoPrice(double goodsinfoPrice) {
		GoodsinfoPrice = goodsinfoPrice;
	}

	public double getGoodsinfosum() {
		return Goodsinfosum;
	}

	public void setGoodsinfosum(double goodsinfosum) {
		Goodsinfosum = goodsinfosum;
	}

	public int getGoodsinfoID() {
		return GoodsinfoID;
	}

	public void setGoodsinfoID(int goodsinfoID) {
		GoodsinfoID = goodsinfoID;
	}

	public double getGoodsWeight() {
		return GoodsWeight;
	}

	public void setGoodsWeight(double goodsWeight) {
		GoodsWeight = goodsWeight;
	}

	public String getGoodsinfoAttrName() {
		return GoodsinfoAttrName;
	}

	public void setGoodsinfoAttrName(String goodsinfoAttrName) {
		GoodsinfoAttrName = goodsinfoAttrName;
	}

	public String getGoodsinfoAttrValue() {
		return GoodsinfoAttrValue;
	}

	public void setGoodsinfoAttrValue(String goodsinfoAttrValue) {
		GoodsinfoAttrValue = goodsinfoAttrValue;
	}

	
	
}
